package beans;

public class ProfessorBean {
	
	//Atributos
	private String cpfProfessor, nomeProfessor, telefoneProfessor, dataNascimentoProfessor;
	private String enderecoProfessor, emailProfessor, senhaProfessor;
	
	//Set & Get
	public String getCpfProfessor() {
		return cpfProfessor;
	}
	public void setCpfProfessor(String cpfProfessor) {
		this.cpfProfessor = cpfProfessor;
	}
	public String getNomeProfessor() {
		return nomeProfessor;
	}
	public void setNomeProfessor(String nomeProfessor) {
		this.nomeProfessor = nomeProfessor;
	}
	public String getDataNascimentoProfessor() {
		return dataNascimentoProfessor;
	}
	public void setDataNascimentoProfessor(String dataNascimentoProfessor) {
		this.dataNascimentoProfessor = dataNascimentoProfessor;
	}
	public String getTelefoneProfessor() {
		return telefoneProfessor;
	}
	public void setTelefoneProfessor(String telefoneProfessor) {
		this.telefoneProfessor = telefoneProfessor;
	}
	public String getSenhaProfessor() {
		return senhaProfessor;
	}
	public void setSenhaProfessor(String senhaProfessor) {
		this.senhaProfessor = senhaProfessor;
	}
	public String getEnderecoProfessor() {
		return enderecoProfessor;
	}
	public void setEnderecoProfessor(String enderecoProfessor) {
		this.enderecoProfessor = enderecoProfessor;
	}
	public String getEmailProfessor() {
		return emailProfessor;
	}
	public void setEmailProfessor(String emailProfessor) {
		this.emailProfessor = emailProfessor;
	}  
}
