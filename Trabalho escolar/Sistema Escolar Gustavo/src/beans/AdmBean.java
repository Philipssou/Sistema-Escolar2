package beans;

public class AdmBean {

	//Atributos
	private String cpfAdmin, nomeAdmin, senhaAdmin, telefoneAdmin;
	private String dataNascimentoAdmin, emailAdmin, enderecoAdmin;
	
	//Set and Get
	
	public String getCpfAdmin() {
		return cpfAdmin;
	}
	public void setCpfAdmin(String cpfAdmin) {
		this.cpfAdmin = cpfAdmin;
	}
	public String getNomeAdmin() {
		return nomeAdmin;
	}
	public void setNomeAdmin(String nomeAdmin) {
		this.nomeAdmin = nomeAdmin;
	}
	public String getSenhaAdmin() {
		return senhaAdmin;
	}
	public void setSenhaAdmin(String senhaAdmin) {
		this.senhaAdmin = senhaAdmin;
	}
	public String getTelefoneAdmin() {
		return telefoneAdmin;
	}
	public void setTelefoneAdmin(String telefoneAdmin) {
		this.telefoneAdmin = telefoneAdmin;
	}
	public String getDataNascimentoAdmin() {
		return dataNascimentoAdmin;
	}
	public void setDataNascimentoAdmin(String dataNascimentoAdmin) {
		this.dataNascimentoAdmin = dataNascimentoAdmin;
	}
	public String getEmailAdmin() {
		return emailAdmin;
	}
	public void setEmailAdmin(String emailAdmin) {
		this.emailAdmin = emailAdmin;
	}
	public String getEnderecoAdmin() {
		return enderecoAdmin;
	}
	public void setEnderecoAdmin(String enderecoAdmin) {
		this.enderecoAdmin = enderecoAdmin;
	}
	
}
